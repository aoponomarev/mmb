import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT = path.resolve(__dirname, "..", "..", "..");

const AIS_DIR = path.join(ROOT, "docs", "ais");

function parseFrontmatter(text) {
    const match = text.match(/^---\s*\n([\s\S]*?)\n---/);
    if (!match) return {};
    const yaml = match[1];
    const result = {};
    for (const line of yaml.split("\n")) {
        const colonIdx = line.indexOf(":");
        if (colonIdx === -1) continue;
        const key = line.slice(0, colonIdx).trim();
        let val = line.slice(colonIdx + 1).trim();
        if (val.startsWith("[") && val.endsWith("]")) {
            val = val.slice(1, -1).split(",").map((s) => s.trim().replace(/^["']|["']$/g, ""));
        } else if (val.startsWith('"') || val.startsWith("'")) {
            val = val.slice(1, -1);
        }
        result[key] = val;
    }
    return result;
}

function parseH1(text) {
    const m = text.match(/^#\s+(.+)$/m);
    return m ? m[1].trim() : "Untitled";
}

function generateIndex() {
    if (!fs.existsSync(AIS_DIR)) return `# index-ais: AIS Specifications\n\nNo docs/ais/ folder found.\n`;

    const files = fs.readdirSync(AIS_DIR)
        .filter((f) => f.endsWith(".md"))
        .sort();

    let md = `<!-- DO NOT EDIT MANUALLY! -->
<!-- This file is auto-generated by is/scripts/architecture/generate-index-ais.js -->

# Index: AIS Specifications

> Master index of Architecture & Infrastructure Specifications.
> Auto-generated. Total: ${files.length}

---

`;

    for (const f of files) {
        const absPath = path.join(AIS_DIR, f);
        const content = fs.readFileSync(absPath, "utf8");
        const fm = parseFrontmatter(content);
        const title = parseH1(content);
        const id = fm.id || path.basename(f, ".md");
        const relPath = `ais/${f}`;
        md += `- [${id}](${relPath}) — ${title}\n`;
    }

    return md;
}

function main() {
    console.log("[docs-index] Generating docs/index-ais.md...");
    const content = generateIndex();
    const docsDir = path.join(ROOT, "docs");
    if (!fs.existsSync(docsDir)) {
        fs.mkdirSync(docsDir, { recursive: true });
    }
    const outPath = path.join(docsDir, "index-ais.md");
    fs.writeFileSync(outPath, content, "utf8");
    console.log(`[docs-index] Successfully updated ${outPath}`);
}

main();
