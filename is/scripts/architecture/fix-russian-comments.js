function walk(dir, ext, out = []) {
  if (!fs.existsSync(dir)) return out;
  for (const e of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, e.name);
    if (e.isDirectory()) {
      if (["node_modules", ".git", ".cursor", "docs"].includes(e.name)) continue;
      walk(full, ext, out);
    } else if (e.name.endsWith(ext)) {
      out.push(full);
    }
  }
  return out;
}

let total = 0;
for (const dir of ["core", "app", "is", "shared", "mm"]) {
  const dirPath = path.join(ROOT, dir);
  for (const file of [...walk(dirPath, ".js"), ...walk(dirPath, ".ts")]) {
    let s = fs.readFileSync(file, "utf8");
    if (!/[а-яА-ЯёЁ]/.test(s)) continue;
    let changed = false;
    for (const [ru, en] of REPLACEMENTS) {
      if (s.includes(ru)) {
        s = s.split(ru).join(en);
        changed = true;
      }
    }
    if (changed) {
      fs.writeFileSync(file, s, "utf8");
      total++;
      console.log(path.relative(ROOT, file));
    }
  }
}
console.log(`\nFixed: ${total} files. Run validate-code-comments-english to check.`);
