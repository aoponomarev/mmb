import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT = path.resolve(__dirname, "..", "..", "..");

// Folders to scan for skills
const SKILL_DIRS = [
    path.join(ROOT, "is", "skills"),
    path.join(ROOT, "core", "skills"),
    path.join(ROOT, "app", "skills")
];

function walkMarkdownFiles(dir, result = []) {
    if (!fs.existsSync(dir)) return result;
    for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
        if (entry.isDirectory()) {
            walkMarkdownFiles(path.join(dir, entry.name), result);
        } else if (entry.isFile() && entry.name.endsWith(".md")) {
            result.push(path.join(dir, entry.name));
        }
    }
    return result;
}

function parseTitle(text) {
    // Try to find yaml title
    const titleMatch = text.match(/^title:\s*['"]?([^'"]+)['"]?/m);
    if (titleMatch) return titleMatch[1];
    
    // Try to find H1
    const h1Match = text.match(/^#\s+(.+)$/m);
    if (h1Match) return h1Match[1];
    
    return "Untitled Skill";
}

function generateIndex() {
    const categories = {};
    let totalSkills = 0;

    for (const dir of SKILL_DIRS) {
        if (!fs.existsSync(dir)) continue;
        const category = path.basename(path.dirname(dir)); // 'is', 'core', 'app'
        
        categories[category] = categories[category] || [];
        
        const files = walkMarkdownFiles(dir);
        for (const file of files) {
            const relativePath = path.relative(ROOT, file).split(path.sep).join("/");
            const content = fs.readFileSync(file, "utf8");
            const title = parseTitle(content);
            const id = path.basename(file, ".md");
            
            categories[category].push(`- [${id}](../${relativePath}) — ${title}`);
            totalSkills++;
        }
    }

    let md = `<!-- DO NOT EDIT MANUALLY! -->
<!-- This file is auto-generated by is/scripts/architecture/generate-skills-index.js -->

# Index: Target App Skills

> Master index of all Target App skills.
> Automatically generated by script (Inverted SSOT).
> Total Skills: ${totalSkills}

---

`;

    const categoryOrder = ["is", "core", "app"];
    
    for (const cat of categoryOrder) {
        if (!categories[cat] || categories[cat].length === 0) continue;
        
        const catName = cat.toUpperCase();
        md += `## ${catName} Layer\n\n`;
        
        categories[cat].sort((a, b) => a.localeCompare(b));
        for (const item of categories[cat]) {
            md += `${item}\n`;
        }
        md += `\n`;
    }

    return md;
}

function main() {
    console.log("[docs-index] Generating docs/skills-index.md...");
    const content = generateIndex();
    const docsDir = path.join(ROOT, "docs");
    if (!fs.existsSync(docsDir)) {
        fs.mkdirSync(docsDir, { recursive: true });
    }
    const outPath = path.join(docsDir, "skills-index.md");
    fs.writeFileSync(outPath, content, "utf8");
    console.log(`[docs-index] Successfully updated ${outPath}`);
}

main();
